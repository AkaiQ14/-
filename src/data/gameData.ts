export function shuffleArray<T>(items: T[]): T[] {
  const shuffled = [...items]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return shuffled
}

export function getCategory(id: string): Category | undefined {
  return CATEGORIES.find((c) => c.id === id)
}

export function getQuestionsForCategory(categoryId: string): Question[] {
  return QUESTIONS.filter((q) => q.categoryId === categoryId)
}

export function getQuestionsForTier(categoryId: string, points: PointTier): Question[] {
  return QUESTIONS.filter((q) => q.categoryId === categoryId && q.points === points)
}

export function pickBoardQuestions(
  categoryId: string,
  points: PointTier,
  count: number
): Question[] {
  const pool = getQuestionsForTier(categoryId, points)
  return shuffleArray(pool).slice(0, Math.min(count, pool.length))
}

export function getQuestion(id: string): Question | undefined {
  return QUESTIONS.find((q) => q.id === id)
}

export function pickRandomCategories(count: number): string[] {
  return shuffleArray(ACTIVE_CATEGORIES).slice(0, count).map((c) => c.id)
}
